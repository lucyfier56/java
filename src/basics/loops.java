package basics;

public class loops {
    public static void main(String[] args) {


        System.out.println("Enter your name: ");



        int x = 0;
        do {
            x++;
        }
        while (x<=10);
        int y = 0;
        while (y<10) {
            System.out.println(y);
            y++;
        }
        int z = 10;
        for(int i=0; i<z; i++){
            System.out.println(i);
        }
    }


}
